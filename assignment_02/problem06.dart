// 6. Convert String to bool:

// string to bool convert
void main() {
  // bool true
  String strbool = 'true';
  // using .toLowerCase() -true
  bool bvalue = (strbool.toLowerCase() == 'true');
  // convert String to boolean
  print(bvalue);

  // bool false
  String strb = 'false';
  // using .toUpperCase - false
  bool strValue = (strb.toUpperCase() == 'false');
  // convert String to boolean
  print(strValue);
}
